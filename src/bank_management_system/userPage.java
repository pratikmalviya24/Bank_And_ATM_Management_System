    private void getTransactionDetails() throws SQLException {
        String query = "select date, type,amount from Transaction where mobileNumber = '"+mobileNumber+"'  ";
        ResultSet result = connection.s.executeQuery(query);
        String[] columnsFields = {"Transaction_Date", "Transaction_Type", "Transaction_Amount"};
        Vector<String> columnFieldName = new Vector<>(Arrays.asList(columnsFields));
        Vector<Vector<Object>> tableData = new Vector<>();
        while (result.next()) {
            tableData.add(new Vector<>(Arrays.asList(
                    result.getString("date"),
                    result.getString("type"),
                    result.getString("amount"))));
        }
        DefaultTableModel tableModel = new DefaultTableModel(tableData, columnFieldName);

        transactionTable.setModel(tableModel);
    }
