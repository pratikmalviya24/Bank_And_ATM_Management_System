    private void getAccountDetails() throws SQLException {
        String query = "select * from Account where accountNumber = '" + accountNumber + "'";
        ResultSet result = connection.s.executeQuery(query);
        result.next();
        String accountType = result.getString("accountType");
        String cardNumber = result.getString("cardNumber");
        String services[] = result.getString("services").split("/");
        HashSet<String> hs = new HashSet<>();
        for (String s : services) {
            if(!hs.contains(s)){
                userServicesComboBox.addItem(s);
                hs.add(s);
            }
           
        }

        userAccountNumber.setText(accountNumber);
        userAccountNumber.setEnabled(false);
        userAccountType.setText(accountType);
        userAccountType.setEnabled(false);
        userCardNumber.setText(cardNumber);
        userCardNumber.setEnabled(false);
    }

