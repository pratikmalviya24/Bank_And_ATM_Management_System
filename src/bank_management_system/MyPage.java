    private void transferButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transferButtonActionPerformed
        // TODO add your handling code here:
        String from = mt_fromTextField.getText().trim();
        String to = toTextField.getText().trim();
        String from_mobileNumber = getMobileNumber(from);
        String to_mobileNumber = getMobileNumber(to);
        String type = "Debited";
        Date date = new Date();
        if (from_mobileNumber.length() == 0 || to_mobileNumber.length() == 0) {
            JOptionPane.showMessageDialog(null, "Account Does Not exist");
            return;
        }
        double amount = Double.parseDouble(amountTranferTextField.getText());
        ValidationCheck v = new ValidationCheck();
        if (v.isValidAccountNumber(from) && v.isValidAccountNumber(to)) {
            try {
                String query = "select * from Balance where accountNumber = '" + from + "'";
                ResultSet result = connection.s.executeQuery(query);

                if (result.next()) {
                    double availableBalance = Double.parseDouble(result.getString("balance"));
                    if (availableBalance > amount) {

                        String finalBalance = (availableBalance - amount) + "";
                        query = "update Balance set balance = '" + finalBalance + "' where accountNumber= '" + from + "'";
                        connection.s.executeLargeUpdate(query);
                        String query1 = "insert into Transaction values('" + from_mobileNumber + "','" + date + "','" + type + "','" + amount + "')";
                        connection.s.executeUpdate(query1);
                        query = "select * from Balance where accountNumber = '" + to + "'";
                        ResultSet result1 = connection.s.executeQuery(query);
                        if (result1.next()) {
                            type = "Credited";

                            double currBalance = Double.parseDouble(result1.getString("balance"));
                            finalBalance = (currBalance + amount) + "";
                            query = "update Balance set balance = '" + finalBalance + "' where accountNumber= '" + to + "'";
                            connection.s.executeLargeUpdate(query);
                            query1 = "insert into Transaction values('" + to_mobileNumber + "','" + date + "','" + type + "','" + amount + "')";
                            connection.s.executeUpdate(query1);
                            JOptionPane.showMessageDialog(null, "Money Transfer Successfully!");
                        } else {
                            JOptionPane.showMessageDialog(null, "Something went Wrong!");
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Insufficient Balance");
                    }
                }
            } catch (SQLException e) {
                System.out.println(e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please Enter Valid Account Number!");
        }
    }//GEN-LAST:event_transferButtonActionPerformed

